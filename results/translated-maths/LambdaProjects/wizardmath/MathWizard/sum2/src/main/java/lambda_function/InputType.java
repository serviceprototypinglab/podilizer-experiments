package lambda_function;

import wizardmath.*;

public class InputType {

    public int s1;

    public int s2;

    public InputType() {
    }

    public void setS1(int s1) {
        this.s1 = s1;
    }

    public int getS1() {
        return s1;
    }

    public void setS2(int s2) {
        this.s2 = s2;
    }

    public int getS2() {
        return s2;
    }

    public InputType(int s1, int s2) {
        this.s1 = s1;
        this.s2 = s2;
    }
}
